using System.Text;
using MackySoft.Json.Canonicalization;
using NUnit.Framework;

#nullable enable

namespace MackySoft.Ucli.Unity.Tests
{
    public sealed class JsonCanonicalizationUnityRuntimeTests
    {
        private const string OfficialValuesInputJson = "{\n  \"numbers\": [333333333.33333329, 1E30, 4.50, 2e-3, 0.000000000000000000000000001],\n  \"string\": \"\\u20ac$\\u000F\\u000aA'\\u0042\\u0022\\u005c\\\\\\\"\\/\",\n  \"literals\": [null, true, false]\n}";

        [Test]
        [Category("Size.Small")]
        public void Canonicalize_WhenOfficialValuesVectorRunsInUnity_ReturnsExpectedUtf8 ()
        {
            var inputUtf8 = Encoding.UTF8.GetBytes(OfficialValuesInputJson);
            var expectedUtf8 = new byte[]
            {
                0x7b, 0x22, 0x6c, 0x69, 0x74, 0x65, 0x72, 0x61, 0x6c, 0x73, 0x22, 0x3a, 0x5b, 0x6e, 0x75, 0x6c,
                0x6c, 0x2c, 0x74, 0x72, 0x75, 0x65, 0x2c, 0x66, 0x61, 0x6c, 0x73, 0x65, 0x5d, 0x2c, 0x22, 0x6e,
                0x75, 0x6d, 0x62, 0x65, 0x72, 0x73, 0x22, 0x3a, 0x5b, 0x33, 0x33, 0x33, 0x33, 0x33, 0x33, 0x33,
                0x33, 0x33, 0x2e, 0x33, 0x33, 0x33, 0x33, 0x33, 0x33, 0x33, 0x2c, 0x31, 0x65, 0x2b, 0x33, 0x30,
                0x2c, 0x34, 0x2e, 0x35, 0x2c, 0x30, 0x2e, 0x30, 0x30, 0x32, 0x2c, 0x31, 0x65, 0x2d, 0x32, 0x37,
                0x5d, 0x2c, 0x22, 0x73, 0x74, 0x72, 0x69, 0x6e, 0x67, 0x22, 0x3a, 0x22, 0xe2, 0x82, 0xac, 0x24,
                0x5c, 0x75, 0x30, 0x30, 0x30, 0x66, 0x5c, 0x6e, 0x41, 0x27, 0x42, 0x5c, 0x22, 0x5c, 0x5c, 0x5c,
                0x5c, 0x5c, 0x22, 0x2f, 0x22, 0x7d,
            };

            var actualUtf8 = Rfc8785JsonCanonicalizer.Canonicalize(inputUtf8);

            Assert.That(actualUtf8, Is.EqualTo(expectedUtf8));
        }

        [TestCase("-0")]
        [TestCase("-0.0")]
        [Category("Size.Small")]
        public void Canonicalize_WhenRawNegativeZeroRunsInUnity_ThrowsClassifiedFailure (string json)
        {
            var exception = Assert.Throws<JsonCanonicalizationException>(() =>
                Rfc8785JsonCanonicalizer.Canonicalize(Encoding.UTF8.GetBytes(json)));

            Assert.That(exception!.FailureKind, Is.EqualTo(JsonCanonicalizationFailureKind.NegativeZero));
        }
    }
}
